# Multiplication, division, addition, and subtraction
print(3*8/2+5-1)

# Exponents
print(4**6)  # Syntax means 4 to the power of 6
print(4**2)  # To square a number
print(4**3)  # To cube a number
print(4**0.5)  # To find the square root of a number

